    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        //Xử lý thêm phòng
        try {
            String tenPhong = jTextField1.getText();
            if (tenPhong.isEmpty()) {
                throw new Exception("Vui lòng nhập tên phòng");
            }
            if (dbc.kiemTraPhong(tenPhong)) {
                throw new Exception("Phòng đã tồn tại");
            }
            String loaiPhong = (String) jComboBox4.getSelectedItem();
            dbc.themPhong(tenPhong, loaiPhong); //Gọi phương thức thêm phòng
            showQuanLyPhong();
            showBangPhongTrong();
            jTextField1.setText("");
            jTextField1.setEditable(true);
            jTextField1.setBackground(Color.WHITE);
            jComboBox4.setSelectedIndex(0);
            JOptionPane.showMessageDialog(rootPane, "Thêm phòng thành công!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

