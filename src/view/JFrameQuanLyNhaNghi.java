    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String tenPhong = jTextField1.getText();
            if (tenPhong.isEmpty()) {
                throw new Exception("Vui lòng nhập tên phòng");
            }
            if (!dbc.kiemTraPhong(tenPhong)) {
                throw new Exception("Không thể sửa vì phòng không tồn tại"); //Kiểm tra các trường dữ liệu
            }
            String loaiPhong = (String) jComboBox4.getSelectedItem();
            int choice = JOptionPane.showConfirmDialog(rootPane, "Phòng " + tenPhong + " sẽ được cập nhật thành " + loaiPhong);
            if (choice == JOptionPane.YES_OPTION) {//Hiển thị thông báo, yêu cầu xác nhận
                dbc.suaPhong(tenPhong, loaiPhong); // Gọi phương thức sửa phòng
                showQuanLyPhong();
                showBangPhongTrong();
                jTextField1.setText("");
                jTextField1.setEditable(true);
                jTextField1.setBackground(Color.WHITE);
                jComboBox4.setSelectedIndex(0);
                JOptionPane.showMessageDialog(rootPane, "Sửa thành công!");
            } else {
                return;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

