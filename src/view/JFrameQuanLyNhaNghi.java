    double soGio(int soPhut) {
        if (soPhut < 60) {
            return 1;
        }
        if (soPhut % 60 == 0) {
            return soPhut / 60;
        }
        if (soPhut % 60 <= 30) {
            return soPhut / 60 + 0.5;
        }
        return soPhut/60 + 1;
    }
