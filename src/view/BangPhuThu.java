    void showTongPhuThu() {
        try {
            jTextField4.setText(Integer.toString((int) dbc.layPhuThu(idPhongDat)));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }

    void showSanPham() {
        try {
            dtmSanPham.setRowCount(0);
            List<Object[]> o = dbc.layThongTinSanPham();
            for (Object[] i : o) {
                dtmSanPham.addRow(i);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }

    void showSanPham(String sp) {
        try {
            dtmSanPham.setRowCount(0);
            List<Object[]> o = dbc.layThongTinSanPham(sp);
            for (Object[] i : o) {
                dtmSanPham.addRow(i);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }

    void showBangPhuThu() {
        try {
            dtmBangPhuThu.setRowCount(0);
            List<Object[]> o = dbc.layThongTinPhuThu(idPhongDat);
            for (Object[] i : o) {
                dtmBangPhuThu.addRow(i);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }

