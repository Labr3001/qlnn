    private void jTableSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSanPhamMouseClicked
        if (jTableSanPham.getSelectedRow() == -1) {
            return;
        }
        int index = jTableSanPham.getSelectedRow();
        jTextField1.setText((String) jTableSanPham.getValueAt(index, 1));
        jTextField5.setText(Integer.toString((int) jTableSanPham.getValueAt(index, 0)));
        jTextField2.setText((Integer.toString((int) jTableSanPham.getValueAt(index, 2))));
        jTextField5.setEditable(false);
        jTextField2.setEditable(false);

    }//GEN-LAST:event_jTableSanPhamMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String soLuongString = jTextField3.getText();
            if (!soLuongString.matches("[0-9]+")) {
                throw new Exception("Số lượng không hợp lệ");
            }
            String maSanPham = jTextField5.getText();
            if (maSanPham.isEmpty()) {
                throw new Exception("Vui lòng chọn sản phẩm trên bảng");
            }
            int soluong = Integer.parseInt(soLuongString);
            if (soluong <= 0) {
                throw new Exception("Số lượng không hợp lệ");
            }
            dbc.themPhuThu(idPhongDat, maSanPham, soLuongString);
            showBangPhuThu();
            showTongPhuThu();
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

